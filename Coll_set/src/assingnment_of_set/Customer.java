package assingnment_of_set;

public class Customer {

	int Custom_id;
	String Sustom_name;
	int Custom_number;
	public Customer(int custom_id, String sustom_name, int custom_number) {
		super();
		Custom_id = custom_id;
		Sustom_name = sustom_name;
		Custom_number = custom_number;
	}
	public int getCustom_id() {
		return Custom_id;
	}
	public void setCustom_id(int custom_id) {
		Custom_id = custom_id;
	}
	public String getCustom_name() {
		return getCustom_name();
	}
	public void setCustom_name(String custom_name) {
		Sustom_name = custom_name;
	}
	public int getCustom_number() {
		return Custom_number;
	}
	public void setCustom_number(int custom_number) {
		Custom_number = custom_number;
	}
	
}
